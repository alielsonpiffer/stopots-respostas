function responder() {
  var letra = document.querySelector("#letter span").textContent.toUpperCase();

  console.log(letra);

  document.querySelectorAll(".answers label").forEach((label, i, l) => {
    label.style.display = "inline-flex";

    var tema = label
      .getElementsByTagName("span")
      .item(0)
      .textContent.trim()
      .replaceAll("\n", "");

    let opcoes = getAnswer(letra, tema);
    let div = document.createElement("div");

    opcoes
      .filter((quantity, index, self) => self.indexOf(quantity) === index) // distinct
      .forEach((opcao) => {
        let p = document.createElement("p");
        p.append(opcao);
        div.append(p);
      });

    div.classList.add("popup");
    label.append(div);
  });

  document.querySelector(".answers > div").style.display = "inline-block";

  document.getElementById("screens").style.height = "100%";
  document.getElementById("screens").style.display = "inline-flex";

  console.log("fim");
}

function getAnswer(letra, tema) {
  console.log(`pegando resposta: ${letra} - ${tema}`);
  return dicionarios
    .filter((dicionario) => {
      return dicionario.keys.some((key) => {
        return wildTest(key, tema);
      });
    })
    .map((dicionario) => {
      return dicionario.values[letra];
    })
    .filter((value) => {
      return !!value;
    });
}

function wildTest(wildcard, str) {
  let w = wildcard.replace(/[.+^${}()|[\]\\]/g, "\\$&");
  const re = new RegExp(
    `^${w.replace(/\*/g, ".*").replace(/\?/g, ".?")}$`,
    "i"
  );
  return re.test(str);
}

